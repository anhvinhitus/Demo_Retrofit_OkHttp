	public static String getResourceName(int pResourceID) {
		if (ZingMobilePayApplication.getInstance() != null) {
			return ZingMobilePayApplication.getInstance().getResources().getResourceEntryName(pResourceID);
		}
		return null;
	}

	private static int get(String pName, String pDef) {
		if (ZingMobilePayApplication.getInstance() != null) {
			int ret = ZingMobilePayApplication.getInstance().getBaseContext().getResources()
					.getIdentifier(pName, pDef, ZingMobilePayApplication.getInstance().getPackageName());
//			Log.d("Resource", pDef + pName + ret);
			return ret;
		} else {
			Log.e("Resource:", pDef + ':' + pName + ":0");
			return 0;
		}
	}
